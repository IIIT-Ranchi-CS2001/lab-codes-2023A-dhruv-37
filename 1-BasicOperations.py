a = float(input("Enter a number : "))
b = float(input("Enter a number : "))

# Addition of two numbers
c = a + b
print("Addition of two numbers is", c)

# Subtraction of two numbers
d = a - b
print("Subtraction of two numbers is", d)

# Multiplication of two numbers
e = a * b
print("Multiplication of two numbers is", e)

# Fraction Quotient of two numbers
f = a / b
print("Division of two numbers is", f)

# Integer Quotient of two numbers
g = a // b
print("Integer Division of two numbers is", g)

# Remainder of two numbers
h = a % b
print("Remainder of two numbers is", h)